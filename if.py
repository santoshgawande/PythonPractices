#Conditional Statement 
#If
if True != (False):
    print('True')

#if-else statement :
if 1 > 4 :
    print('1 is greater')
else :
    print('4 is greater')

#Nested if-else or if-elif-else:
# This also called as Proxy for Switch Case Statements. We can write this way.
if 10 < 4 :
    print('10  is greater')
elif 10 < 50:
    print('10 is less than 50')
else :
    print('10 is not no.')